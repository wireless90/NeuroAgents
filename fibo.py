def fibonacci(n):
    if n <= 0:
        return "Input should be a positive integer."
    elif n == 1:
        return 0
    elif n == 2:
        return 1
    else:
        a, b = 0, 1
        for _ in range(2, n):
            a, b = b, a + b
        return b

# Calculate the 15th Fibonacci number
fibonacci_15 = fibonacci(15)
print(f"The 15th Fibonacci number is: {fibonacci_15}")
